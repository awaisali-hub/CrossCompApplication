package com.eclairios.CrossComps.Model;

public class ModelChallenge {

    String challengerName,challengerScore;
    String challengerGender,challengerAge,challengerAddress;

    public ModelChallenge() {
    }

    public String getChallengerName() {
        return challengerName;
    }

    public void setChallengerName(String challengerName) {
        this.challengerName = challengerName;
    }

    public String getChallengerScore() {
        return challengerScore;
    }

    public void setChallengerScore(String challengerScore) {
        this.challengerScore = challengerScore;
    }

    public String getChallengerGender() {
        return challengerGender;
    }

    public void setChallengerGender(String challengerGender) {
        this.challengerGender = challengerGender;
    }

    public String getChallengerAge() {
        return challengerAge;
    }

    public void setChallengerAge(String challengerAge) {
        this.challengerAge = challengerAge;
    }

    public String getChallengerAddress() {
        return challengerAddress;
    }

    public void setChallengerAddress(String challengerAddress) {
        this.challengerAddress = challengerAddress;
    }
}
